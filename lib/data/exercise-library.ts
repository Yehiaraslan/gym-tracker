export function getExerciseByName(name: string): ExerciseLibraryEntry | undefined {
  return EXERCISE_LIBRARY.find(e => e.name.toLowerCase() === name.toLowerCase());
}

export function getExercisesByMuscleGroup(group: string): ExerciseLibraryEntry[] {
  return EXERCISE_LIBRARY.filter(e => e.muscleGroup === group);
}
